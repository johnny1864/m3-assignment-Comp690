let prompt1 = window.prompt('Enter a language code');


if(prompt1 == "fr"){
    window.alert(`Hello World translated in French is: Bonjour le monde`);
} else if(prompt1 == "es"){
    window.alert(`Hello World translated in Spanish is: Hola Mundo`);
} else if(prompt1 == "de"){
    window.alert(`Hello World translated in Germany is: Hallo Welt`);
}else if(prompt1 == "en") {
    window.alert(`Hello World translated in English is: Hello World`);
} else {
    window.alert(`Hello World translated in English is: Hello World`);
}